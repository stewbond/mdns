class IfIndex
{
public:
    using Type = int;

    constexpr IfIndex(Type index) : m_value(index) {}
    constexpr Type value() const noexcept { return m_value; }
    static constexpr IfIndex any() noexcept { return IfIndex(-1); }
    constexpr auto operator<=>(const IfIndex&) const noexcept = default;
private:
    int m_value;
    friend std::ostream& operator<<(std::ostream& o, const IfIndex& i);
};


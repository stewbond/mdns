typedef std::list<std::string> StrList;

struct DeviceInfo
{
    IfIndex interface = IfIndex::any();
    Protocol protocol = Protocol::unspec;
};

// Generated by DomainBrowser
// Input to DomainBrowser, ServiceTypeBrowser, HostNameResolver, AddressResolver
struct DomainInfo : public DeviceInfo
{
    std::string domain = "local";
};

// Generated by ServiceTypeBrowser
// Input to ServiceBrowser
struct TypeInfo : public DomainInfo
{
    std::string type;
};

// Generated by ServiceBrowser
// Input to ServiceResolver
struct ServiceInfo : public TypeInfo
{
    std::string name;
};

struct ServiceDetails : ServiceInfo
{
    std::string hostname;
    uint16_t port;
    StrList txt;
};

// Needed to publish a service
struct ServiceRecord : ServiceDetails
{
    StrList subtypes;
};

// Generated by ServiceResolver
struct ResolvedServiceInfo : public ServiceDetails
{
    Address addr;
};

// Generated by AddressResolver, HostNameResolver
// Input to Group::AddHost() to publish a Hostname/Address pair
struct AddressHostnameInfo : public DeviceInfo
{
    std::string hostname;
    Address addr;
};

// Input to RecordBrowser
struct RecordRequest : public DeviceInfo
{
    std::string name; // Name of record (hostname?)
    DnsType type; // Type of record A,AAAA,CNAME,PTR,TXT,MX,NS,SOA,HINFO,SRV
};

// Generated by RecordBrowser
struct RecordResult : public RecordRequest
{
    // Ex: case AVAHI_DNS_TYPE_A:    4-byte in_addr
    //     case AVAHI_DNS_TYPE_AAAA: 16-byte in_addr
    //     case AVAHI_DNS_TYPE_PTR:  can be decoded with avahi_dns_packet_consume_name()
    const void *rdata; // raw DNS data.  Size/contents changes based on type
    size_t size; // size of rdata
};

// Input to Group::AddRecord().  Publish a low-level record
struct DnsRecord : public RecordResult
{
    uint32_t ttl;
};

